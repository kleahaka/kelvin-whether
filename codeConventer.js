//degree in Kalvin
const kelvin = 0;
//convert in celsius
var celsius = kelvin - 273;
//conver in fahrenheit
//rounded value in fahrenheit
let fahrenheit = celsius * (9 / 5) + 32;
fahrenheit = Math.floor(fahrenheit);
console.log(`The temperature is ${fahrenheit} degrees Fahrenheit`);
